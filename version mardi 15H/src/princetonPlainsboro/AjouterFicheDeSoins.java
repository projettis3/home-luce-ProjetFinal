    //nom du patient (verifie que le patient existe et remplie l'attribut patient)
    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        int i = 0;
        while (i < dm.renvoieListePatients().size() && !jTextField1.getText().equals(dm.renvoieListePatients().get(i).getNom())) {
            i++;
        }
        if (i >= dm.renvoieListePatients().size()) {
            JOptionPane.showMessageDialog(null, "le nom du patient est incorrect", "Erreur", JOptionPane.ERROR_MESSAGE);
        } else {
            patient = new Patient(dm.renvoieListePatients().get(i).getNom(), dm.renvoieListePatients().get(i).getPrenom(), dm.renvoieListePatients().get(i).getNumSecu(), dm.renvoieListePatients().get(i).getCle(), dm.renvoieListePatients().get(i).getDateDeNaissance(), dm.renvoieListePatients().get(i).getAdresse());
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

