    //nom medecin
    private void jTextField2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField2ActionPerformed
        int i = 0;
        while (i < dm.renvoieListeMedecins().size() && !jTextField2.getText().equals(dm.renvoieListeMedecins().get(i).getNom())) {
            i++;
        }
        if (i >=dm.renvoieListeMedecins().size()) {
            JOptionPane.showMessageDialog(null, "le nom du medecin est incorrect", "Erreur", JOptionPane.ERROR_MESSAGE);
            medecin = null;
        } else {
            medecin = new Medecin(dm.renvoieListeMedecins().get(i).getPrenom(), dm.renvoieListeMedecins().get(i).getNom(), (Specialite) dm.renvoieListeMedecins().get(i).getSpecialite(), dm.renvoieListeMedecins().get(i).getNumTel(), dm.renvoieListeMedecins().get(i).getMotDePasse());
        }
    }//GEN-LAST:event_jTextField2ActionPerformed

