    private void initjList1() {
        jList1.setModel(dm.getModelPatient());
        jList1.addListSelectionListener(new ListSelectionListener() {

           
            @Override
            public void valueChanged(ListSelectionEvent e) {
                List<Patient> patientsSelectionnes = new Vector<Patient>();
                patientsSelectionnes = jList1.getSelectedValuesList();

                DossierMedical dossier = new DossierMedical();
                Iterator<FicheDeSoins> it = dm.getFiches().iterator();
                while (it.hasNext()) {
                    FicheDeSoins fiche = it.next();
                    if (patientsSelectionnes.contains(fiche.getPatient())) {
                       dossier.getFiches().add(fiche);
                    }
                }
                jTextArea1.setText(dossier.afficherString());
            }
        });
    }

