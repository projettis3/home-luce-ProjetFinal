    //le bouton du tri par medecin des patients
    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        if (jTextField1.getText() != null) {
            jTextField1ActionPerformed(evt); //verification du nom de medecin
            DefaultListModel model = new DefaultListModel();
            DossierMedical dossier = new DossierMedical();
            Iterator<FicheDeSoins> it = dm.getFiches().iterator();
            while (it.hasNext()) {
                FicheDeSoins fiche = it.next();
                if (fiche.getMedecin().getNom().equals(jTextField1.getText())) {
                    model.addElement(fiche.getPatient());
                    dossier.getFiches().add(fiche);
                }
            }
            jList1.setModel(model);
            jTextArea1.setText(dossier.afficherString());
        } else if (!(jTextField1.getText() != null)) {
            jList1.setModel(dm.getModelPatient());
            jTextArea1.setText(dm.afficherString());
        }
    }//GEN-LAST:event_jButton8ActionPerformed

